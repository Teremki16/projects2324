$("button").click(function(){
    $(".box").animate({
        height: "300px",
        opacity: "0.5"
    })
    $(".box").animate({
        width: "300px",
        opacity: "1"
    })
    $(".box").animate({
        height: "100px",
        opacity: "0.5"
    })
    $(".box").animate({
        width: "100px",
        opacity: "1"
    })
})